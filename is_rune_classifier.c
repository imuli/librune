static int classifications_compare(const void *key, const void *array){
	Rune r = ((const struct classifications*)key)->r;
	const struct classifications *class = array;
	if(r < class->r) return -1;
	if(r >= (class+1)->r) return 1;
	return 0;
};

int is_rune_classifier(Rune r){
	struct classifications *class, key;
	key.r = r;
	class = bsearch(&key, unicode_classes, nelem(unicode_classes)-1,
			sizeof(struct classifications), classifications_compare);
	if(class == NULL) exit(2);
	return class_map[(int) class->class[(r-class->r)%4]];
}

